
// Função de soma com return

function somaEntreCincoNumeros(firstnumber,secondnumber,thirdnumber,fornumber,fivenumber){

return firstnumber + secondnumber + thirdnumber + fornumber + fivenumber
}

const valorDaSomaEntreCincoNumeros = somaEntreCincoNumeros(68,95,83,40,72)

console.log("A soma dos cinco números é:" + valorDaSomaEntreCincoNumeros)

// Função de divisão com return

function divisaoEntreCincoNumeros(firstnumber,secondnumber,thirdnumber,fournumber,fivenumber){

return firstnumber / secondnumber / thirdnumber / fournumber / fivenumber
}

const valorDaDivisaoEntreCincoNumeros = divisaoEntreCincoNumeros(180,68,110,28,40)

console.log("A divisão dos cinco números é:" + valorDaDivisaoEntreCincoNumeros)

